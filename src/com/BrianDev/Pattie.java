package com.BrianDev;

public class Pattie {

    private String type;
    private int quantity;

    public Pattie(String type, int quantity) {
        this.type = type;
        this.quantity = quantity;
    }

    public String getType() {
        return type;
    }

    public int getQuantity() {
        return quantity;
    }
}
